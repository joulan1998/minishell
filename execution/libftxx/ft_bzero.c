#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*res;

	res = s;
	i = 0;
	while (n > 0 && i < n)
	{
		res[i] = 0;
		i++;
	}
	return ;
}
